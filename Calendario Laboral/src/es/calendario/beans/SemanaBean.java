package es.calendario.beans;


public class SemanaBean {
	
	private DiaBean lunes;
	private DiaBean martes;
	private DiaBean miercoles;
	private DiaBean jueves;
	private DiaBean viernes;
	private DiaBean sabado;
	private DiaBean domingo;
	public DiaBean getLunes() {
		return lunes;
	}
	public void setLunes(DiaBean lunes) {
		this.lunes = lunes;
	}
	public DiaBean getMartes() {
		return martes;
	}
	public void setMartes(DiaBean martes) {
		this.martes = martes;
	}
	public DiaBean getMiercoles() {
		return miercoles;
	}
	public void setMiercoles(DiaBean miercoles) {
		this.miercoles = miercoles;
	}
	public DiaBean getJueves() {
		return jueves;
	}
	public void setJueves(DiaBean jueves) {
		this.jueves = jueves;
	}
	public DiaBean getViernes() {
		return viernes;
	}
	public void setViernes(DiaBean viernes) {
		this.viernes = viernes;
	}
	public DiaBean getSabado() {
		return sabado;
	}
	public void setSabado(DiaBean sabado) {
		this.sabado = sabado;
	}
	public DiaBean getDomingo() {
		return domingo;
	}
	public void setDomingo(DiaBean domingo) {
		this.domingo = domingo;
	}
	
	

}
