package es.calendario.beans;

public class DiaBean {

	private String diaDeLaSemana;
	private int numDiaMes;
	private String claseDia; //Normal, Libre
	
	public String getDiaDeLaSemana() {
		return diaDeLaSemana;
	}
	public void setDiaDeLaSemana(String diaDeLaSemana) {
		this.diaDeLaSemana = diaDeLaSemana;
	}
	public int getNumDiaMes() {
		return numDiaMes;
	}
	public void setNumDiaMes(int numDiaMes) {
		this.numDiaMes = numDiaMes;
	}
	public String getClaseDia() {
		return claseDia;
	}
	public void setClaseDia(String claseDia) {
		this.claseDia = claseDia;
	}
	
	
}
